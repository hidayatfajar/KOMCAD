export default {
  getLoading: state => (name) => {
    if (state.loading !== null) {
      return state.loading[name] !== undefined ? state.loading[name] : false
    } else {
      return false
    }
  },
  getWarnaTema (state) {
    return state.kode_warna_hexa
  },
  getCaptcha (state) {
    return state.captcha
  },
  getHakAksesModul (state) {
    return state.hak_akses_modul
  },
  getHakAksesFitur (state) {
    return state.hak_akses_fitur
  },
  getAuth (state) {
    return state.auth
  },
  getModule (state) {
    return state.module
  },
  getLoadingApp (state) {
    return state.loading_app
  }
}
